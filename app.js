function setLang(lang) {
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const k = el.getAttribute('data-i18n');
    if (i18n[lang][k] !== undefined) el.textContent = i18n[lang][k];
  });
  if (window.currentStrain && window.updateStrainStats) {
    window.updateStrainStats(window.currentStrain);
  }
  const labels = { en: 'EN', th: 'ไทย', ru: 'RU' };
  document.getElementById('lang-current').textContent = labels[lang];
  document.querySelectorAll('.lang-option').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  closeLangDropdown();
}

function closeLangDropdown() {
  const dd = document.getElementById('lang-dropdown');
  dd.classList.remove('open');
  document.getElementById('lang-trigger').setAttribute('aria-expanded', 'false');
}

(function initNav() {
  const hamburger = document.getElementById('nav-hamburger');
  const navLinks = document.querySelector('.nav-links');
  const langTrigger = document.getElementById('lang-trigger');
  const langDropdown = document.getElementById('lang-dropdown');

  hamburger.addEventListener('click', () => {
    const isOpen = navLinks.classList.toggle('open');
    hamburger.classList.toggle('open', isOpen);
    if (isOpen) closeLangDropdown();
  });

  langTrigger.addEventListener('click', (e) => {
    e.stopPropagation();
    const isOpen = langDropdown.classList.toggle('open');
    langTrigger.setAttribute('aria-expanded', String(isOpen));
  });

  document.addEventListener('click', (e) => {
    if (!langDropdown.contains(e.target)) closeLangDropdown();
    if (!navLinks.contains(e.target) && !hamburger.contains(e.target)) {
      navLinks.classList.remove('open');
      hamburger.classList.remove('open');
    }
  });
})();

/* ── AGE GATE ─────────────────────────────────────────────── */
(function initAgeGate() {
  const gate = document.getElementById('age-gate');
  if (!gate) return;
  const KEY = 'wwp-age-confirmed';
  let stored = null;
  try { stored = localStorage.getItem(KEY); } catch (e) {}
  if (stored === '1') {
    gate.classList.add('hidden');
    return;
  }
  document.body.style.overflow = 'hidden';
  const confirmBtn = gate.querySelector('.age-gate-confirm');
  const declineBtn = gate.querySelector('.age-gate-decline');
  if (confirmBtn) confirmBtn.addEventListener('click', () => {
    try { localStorage.setItem(KEY, '1'); } catch (e) {}
    gate.classList.add('hidden');
    document.body.style.overflow = '';
  });
  if (declineBtn) declineBtn.addEventListener('click', () => {
    window.location.href = 'https://www.google.com/';
  });
})();
