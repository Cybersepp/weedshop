(function initPageScrollReveal() {
  if (typeof gsap === 'undefined' || typeof ScrollTrigger === 'undefined') return;
  if (window.matchMedia('(prefers-reduced-motion: reduce)').matches) return;

  gsap.registerPlugin(ScrollTrigger);

  const ease = 'power3.out';
  const start = 'top 82%';

  function reveal(targets, trigger, options = {}) {
    const els = gsap.utils.toArray(targets);
    if (!els.length) return;

    const from = { y: options.y ?? 32, opacity: 0 };
    if (options.scale != null) from.scale = options.scale;

    const to = {
      y: 0,
      opacity: 1,
      duration: options.duration ?? 0.85,
      stagger: options.stagger ?? 0,
      delay: options.delay ?? 0,
      ease,
    };
    if (options.scale != null) to.scale = 1;

    gsap.set(els, from);
    gsap.to(els, {
      ...to,
      scrollTrigger: {
        trigger: trigger || els[0],
        start: options.start ?? start,
        once: true,
      },
    });
  }

  if (document.querySelector('.about-article')) {
    reveal('.about-lead', '.about-article', { y: 36, duration: 0.9, start: 'top 85%' });
    reveal('.about-divider', '.about-divider', { y: 20, duration: 0.7, start: 'top 88%' });
    reveal('.about-body p', '.about-article', { y: 24, stagger: 0.06, duration: 0.75, start: 'top 86%' });
    reveal('.about-quote', '.about-quote', { y: 32, scale: 0.98, duration: 0.9, start: 'top 85%' });
    reveal('.about-cta-eyebrow, .about-cta-title', '.about-cta', { y: 28, stagger: 0.1 });
    reveal('.about-cta-btns', '.about-cta', { y: 24, duration: 0.75, start: 'top 88%' });
  }

  if (document.querySelector('.partners-intro')) {
    reveal('.partners-lead', '.partners-intro', { y: 36, duration: 0.9, start: 'top 85%' });
    reveal('.partners-intro > p:not(.partners-lead)', '.partners-intro', { y: 28, duration: 0.85, start: 'top 86%' });
    reveal('.partners-stat', '.partners-stats', { y: 40, scale: 0.98, duration: 0.9, stagger: 0.1, start: 'top 85%' });
    reveal('.partners-benefits-header > *', '.partners-benefits-header', { y: 28, stagger: 0.1 });
    reveal('.partners-benefit', '.partners-benefits-grid', { y: 24, stagger: 0.06, duration: 0.8, start: 'top 86%' });
    reveal('.partners-cta-label, .partners-cta-title', '.partners-cta', { y: 28, stagger: 0.1 });
    reveal('.partners-cta-text', '.partners-cta', { y: 24, duration: 0.75, start: 'top 88%' });
    reveal('.partners-cta-actions', '.partners-cta', { y: 32, duration: 0.85, start: 'top 88%' });
  }

  if (document.querySelector('.about-article') || document.querySelector('.partners-intro')) {
    window.addEventListener('load', () => ScrollTrigger.refresh());
  }
})();

(function initHeroTextReveal() {
  const heroLeft = document.querySelector('.hero-left');
  if (!heroLeft || typeof gsap === 'undefined') return;

  const reducedMotion = window.matchMedia('(prefers-reduced-motion: reduce)').matches;
  if (reducedMotion) {
    heroLeft.classList.remove('hero-pending');
    return;
  }

  const eyebrow = heroLeft.querySelector('.hero-eyebrow');
  const headline = heroLeft.querySelector('.hero-headline');
  const sub = heroLeft.querySelector('.hero-sub');
  const ctas = gsap.utils.toArray('.hero-ctas > *', heroLeft);
  const stats = gsap.utils.toArray('.hero-trust-stat', heroLeft);
  const trustValues = gsap.utils.toArray('.hero-trust-value', heroLeft);
  const ease = 'power3.out';
  let played = false;

  function formatTrustValue(val, decimals) {
    return decimals > 0 ? val.toFixed(decimals) : String(Math.round(val));
  }

  function resetTrustCounters() {
    trustValues.forEach((el) => {
      const decimals = parseInt(el.dataset.decimals || '0', 10);
      el.textContent = formatTrustValue(0, decimals);
    });
  }

  function animateTrustCounters() {
    trustValues.forEach((el, i) => {
      const target = parseFloat(el.dataset.value, 10);
      const decimals = parseInt(el.dataset.decimals || '0', 10);
      const state = { val: 0 };

      gsap.to(state, {
        val: target,
        duration: 1.35,
        delay: i * 0.12,
        ease: 'power2.out',
        onUpdate: () => {
          el.textContent = formatTrustValue(state.val, decimals);
        },
      });
    });
  }

  function play() {
    if (played) return;
    played = true;
    heroLeft.classList.remove('hero-pending');
    resetTrustCounters();

    const tl = gsap.timeline({ defaults: { ease } });

    if (eyebrow) {
      gsap.set(eyebrow, { y: 18, opacity: 0, letterSpacing: '0.34em' });
      tl.to(eyebrow, { y: 0, opacity: 1, letterSpacing: '0.24em', duration: 0.7 });
    }

    if (headline) {
      gsap.set(headline, {
        y: 36,
        opacity: 0,
        clipPath: 'inset(0 100% 0 0)',
        filter: 'blur(5px)',
      });
      tl.to(
        headline,
        {
          y: 0,
          opacity: 1,
          clipPath: 'inset(0 0% 0 0)',
          filter: 'blur(0px)',
          duration: 0.95,
          ease: 'power4.out',
        },
        eyebrow ? '-=0.35' : 0
      );
    }

    if (sub) {
      gsap.set(sub, { y: 22, opacity: 0 });
      tl.to(sub, { y: 0, opacity: 1, duration: 0.75 }, '-=0.5');
    }

    if (ctas.length) {
      gsap.set(ctas, { y: 16, opacity: 0 });
      tl.to(ctas, { y: 0, opacity: 1, duration: 0.65, stagger: 0.1 }, '-=0.45');
    }

    if (stats.length) {
      gsap.set(stats, { y: 20, opacity: 0 });
      tl.to(stats, { y: 0, opacity: 1, duration: 0.7, stagger: 0.08 }, '-=0.3');
      if (trustValues.length) {
        tl.call(animateTrustCounters, null, '-=0.45');
      }
    }
  }

  function isGateOpen() {
    const gate = document.getElementById('age-gate');
    return gate && !gate.classList.contains('hidden');
  }

  function tryPlay() {
    if (!isGateOpen()) play();
  }

  const gate = document.getElementById('age-gate');
  const AGE_KEY = 'wwp-age-confirmed';
  let ageVerified = false;
  try { ageVerified = localStorage.getItem(AGE_KEY) === '1'; } catch (e) {}

  if (ageVerified || !gate || gate.classList.contains('hidden')) {
    tryPlay();
    return;
  }

  const confirmBtn = gate.querySelector('.age-gate-confirm');
  if (confirmBtn) {
    confirmBtn.addEventListener('click', tryPlay, { once: true });
  }
})();
